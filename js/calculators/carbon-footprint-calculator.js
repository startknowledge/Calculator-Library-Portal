export default function () {
  const div = document.createElement("div");

  div.innerHTML = `
    <label>Enter your monthly electricity usage (kWh):</label>
    <input type="number" id="carbonValue" placeholder="Enter kWh">

    <button onclick="calculateCarbon()">Calculate</button>
    <p id="carbonResult"></p>
    <br>
    <h3>Carbon Footprint Calculator</h3>
    <p>Estimate the CO2 emissions based on your electricity usage.</p>
    <p><b>Explanation:</b></p>
    <p>The carbon footprint indicates the amount of carbon dioxide (CO2) generated by your energy consumption.</p>
    <p><b>Formula:</b></p>
    <p>CO2 (kg) = Electricity (kWh) Ã— 0.92</p>
    <p><b>Example:</b>  
    If you use <b>100 kWh</b>, your carbon footprint is <b>92 kg CO2</b>.</p>
    <p>Based on the input electricity usage, it calculates the estimated CO2 emissions.</p>
    <p>Note: The factor 0.92 kg CO2 per kWh is an average value and may vary by region.</p>
    <p>It is important to monitor and reduce your carbon footprint to help combat climate change.</p>
    <p>It is advisable to adopt energy-efficient practices to lower your emissions.</p>
    <p><b>Disclaimer:</b> This is an estimate and actual emissions may vary.</p>
    <br>
  `;

  window.calculateCarbon = function () {
    const kwh = parseFloat(document.getElementById("carbonValue").value);
    if (isNaN(kwh)) return;

    const co2 = (kwh * 0.92).toFixed(2);
    document.getElementById("carbonResult").innerHTML =
      `<b>Estimated CO2 Emission:</b> ${co2} kg`;
  };

  return div;
}
